$FormatEnumerationLimit=-1 ; `
Write-host "`n`n`n################################################################################" ; `
Write-host "# View Systeminfo ##############################################################" ; `
Write-host "################################################################################" ; `
Write-host "### Build ######################################################################" ; `
Get-Date ; `
Get-CimInstance win32_operatingsystem | fl caption,version,buildnumber,csname ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### IP-Adresse #################################################################" ; `
ipconfig /all ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "###  Computerinfo ##############################################################" ; `
get-computerinfo ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### LOGONSERVER ################################################################" ; `
nltest /DSGETDC: ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### Roles&Feature ##############################################################" ; `
get-windowsfeature | where installed | ft ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### Update Info ################################################################" ; `
$listupdate=(Get-CimInstance -ClassName Win32_QuickFixEngineering | `
Select-Object -Property HotFixID,Caption,Description,InstalledOn | sort -Descending -Property Installedon) ; `
get-date | Format-Custom ; hostname  ; $listupdate | ft ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### Disk status ################################################################" ; `
Get-CimInstance -Class Win32_Volume | Select-Object Driveletter, Caption,Filesystem, @{n='Size(GB) '; `
e={'{0:N2}' -f ($_.Capacity/1GB)}}, @{n='Free(GB) ';e={'{0:N2}' -f ($_.Freespace/1GB)}}, `
@{n='Free(%) ';e={'{0:N0}' -f (100*$_.Freespace/$_.capacity)}}, Blocksize | ft -AutoSize ; `
Write-host "################################################################################`n`n" ; `
Write-host "################################################################################" ; `
Write-host "### FW Status ##################################################################" ; `
Get-NetFirewallProfile | select name,enabled | ft; Get-NetConnectionProfile | select name,networkcategory | ft ; `
Write-host "################################################################################`n`n`n" ; `
Write-host "################################################################################" ; `
Write-host "# End Systeminfo ###############################################################" ; `
Write-host "################################################################################`n`n" ; `
<# end #>
